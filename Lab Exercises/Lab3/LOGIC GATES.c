#include <stdio.h>

int main() {
    int a=7;
    int b=10;
    //for using AND function use &
    printf("7 and 10 is: %d", a&b);
    // for using OR use |
    printf("\n7 or 10 is: %d", a|b);
    // for shifting right use >>
    printf ("\nRight shift of 64: %d", 64>>3);
    // for shifting left use <<
    printf("\nLeft shift of 64: %d",64<<3);
    //for using XOR use ^
    printf("\n7 xor 10 is :%d", 7^10);
    //for using NOT use ~
    printf("\n not 10:%d", ~b);



}
